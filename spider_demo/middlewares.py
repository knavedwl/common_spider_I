class SpiderDemoIIDownloaderMiddleware:
    # Not all methods need to be defined. If a method is not defined,
    # scrapy acts as if the downloader middleware does not modify the
    # passed objects.

    def __init__(self):
        self.options = firefox_options()

        self.options.page_load_strategy = 'normal'
        # 禁止加载图片
        self.options.set_preference('permissions.default.image', 2)
        # 设置response返回json的时候不处理页面
        self.options.set_preference('devtools.jsonview.enabled', False)
        # 设置最大弹窗数量1000个，不设置的话默认20个，并且关闭拦截弹窗的话也是限制20个
        self.options.set_preference('dom.popup_maximum', 10)
        # self.options.add_argument('--headless')

        super(SpiderDemoIIDownloaderMiddleware, self).__init__()
        self.ua = UserAgent()

    @classmethod
    def from_crawler(cls, crawler):
        # This method is used by Scrapy to create your spiders.
        s = cls()
        crawler.signals.connect(s.spider_opened, signal=signals.spider_opened)
        return s

    def process_request(self, request, spider):
        # Called for each request that goes through the downloader
        # middleware.

        # Must either:
        # - return None: continue processing this request
        # - or return a Response object
        # - or return a Request object
        # - or raise IgnoreRequest: process_exception() methods of
        #   installed downloader middleware will be called
        spider.driver.get(request.url)
        return None

    def process_response(self, request, response, spider):
        # Called with the response returned from the downloader.

        # Must either;
        # - return a Response object
        # - return a Request object
        # - or raise IgnoreRequest

        headers = response.headers

        Cookiese = spider.driver.get_cookies()
        cookie = ''
        for c in Cookiese:
            cookie += c['name']
            cookie += '='
            cookie += c['value']
            cookie += ';'
        headers['Cookie'] = cookie

        url = request.url
        result = True
        # while result:
        #
        #     list = spider.driver.find_elements_by_xpath("//tbody//tr")
        #     if len(list) > 0:
        #         result = False
        body = spider.driver.page_source
        return HtmlResponse(url=url, headers=headers, body=body, encoding='utf-8', request=request)

    # 判断当前窗口加载的页面是否加载完成
    def is_load_complete(self, spider):

        if spider.driver.execute_script('return document.readyState;') == 'complete':
            return True
        else:
            return False

    def process_exception(self, request, exception, spider):
        # Called when a download handler or a process_request()
        # (from other downloader middleware) raises an exception.

        # Must either:
        # - return None: continue processing this exception
        # - return a Response object: stops process_exception() chain
        # - return a Request object: stops process_exception() chain
        pass

    def spider_opened(self, spider):
        spider.logger.info('Spider opened: %s' % spider.name)

        spider.driver = webdriver.Firefox(options=self.options

                                          )  # 指定使用的浏览器
